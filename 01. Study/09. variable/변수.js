// let은 변수를 선언하는 것이다.
// color는 변수의 이름이다.
// 인간에게도 이름이 있듯이 변수도 이름을 지정해줘야 한다.
// 변수의 이름은 겹치면 안된다.
// =는 오른쪽에 있는 값을 왼쪽에다 집어 넣는다.

let color = "black"
// let을 쓰지않으면 값을 바꿀 수 있다.
color = "haha"

// const는 변수의 값을 바꾸고 싶지 않을 때 쓴다.
// 한번 할당을 하면 재할당이 불가능
const color2 = "black"

// 맨 처음 자바스크립트는 var 밖에 없었다.
